# there are different types of testing

# Unit testing
# Integration testing
# System testing
# Acceptance testing

# Unit testing
# Unit testing is the lowest level of software testing where individual units or components of a software are tested. 
# The purpose is to validate that each unit of the software performs as designed. Unit testing is done by the developer.
# focusing on this today

# Integration testing
# Integration testing is a level of software testing where individual software modules are combined and tested as a group.
# The purpose is to expose faults in the interaction between integrated modules. Integration testing is done by the developer.

# System testing
# System testing is a level of software testing where a complete, integrated software system is tested.

# Acceptance testing
# Acceptance testing is a level of software testing where a system is tested for acceptance.
# usually done by the customer or a user, to determine whether or not to accept the system.
# not going to cover this type of testing


